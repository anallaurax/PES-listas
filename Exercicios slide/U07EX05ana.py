'''Exercício 05: Implemente um algoritmo que exiba o preço total
de dois salgados e um suco na cantina (os valores você vai
definir através de variáveis). Utilize comentários no seu código
fonte.'''
salgado1 = int(input("Digite o valor do salgado 1: "))
salgado2 = int(input("Digite o valor do salgado 2 : "))
